class I18nManager {
    constructor() {
        this.currentLang = 'ko'; // default backup
        this.init();
    }

    init() {
        // 1. Detect language (Local storage > Browser settings > default 'ko')
        const stored = localStorage.getItem('pixelcraft_lang');
        if (stored && DICTIONARY[stored]) {
            this.currentLang = stored;
        } else {
            const browserLang = navigator.language || navigator.userLanguage || 'ko';
            const short = browserLang.split('-')[0].toLowerCase();
            
            // Map short code to supported dictionaries
            if (DICTIONARY[short]) {
                this.currentLang = short;
            } else if (short === 'zh') {
                // Determine simplified vs traditional
                if (browserLang.toLowerCase().includes('tw') || browserLang.toLowerCase().includes('hk') || browserLang.toLowerCase().includes('mo')) {
                    this.currentLang = 'zt';
                } else {
                    this.currentLang = 'zh';
                }
            } else {
                this.currentLang = 'en'; // Global fallback
            }
        }
    }

    setLanguage(lang) {
        if (!DICTIONARY[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('pixelcraft_lang', lang);
        this.translateDOM();
        
        // Dispatch custom event so modules can listen and update their internal text
        window.dispatchEvent(new CustomEvent('languagechange', { detail: lang }));
    }

    t(key, replacements = {}) {
        const dict = DICTIONARY[this.currentLang] || DICTIONARY['en'];
        let value = dict[key] || DICTIONARY['en'][key] || key;
        
        // Interpolate variables if provided, e.g. t('size', {val: 10})
        Object.keys(replacements).forEach(k => {
            value = value.replace(`{${k}}`, replacements[k]);
        });
        return value;
    }

    translateDOM() {
        // Find all elements with data-i18n attribute
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.t(key);
            
            // Handle special elements
            if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit')) {
                el.value = translation;
            } else {
                // Preserve child elements (like icons) if they exist, only update text nodes
                const icon = el.querySelector('i[data-lucide]');
                const iconHtml = icon ? icon.outerHTML : '';
                
                // If there's an icon, draw icon first, then text
                if (iconHtml) {
                    el.innerHTML = iconHtml + ` <span>${translation}</span>`;
                } else {
                    el.textContent = translation;
                }
            }
        });

        // Translate placeholders
        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });

        // Translate titles
        const titles = document.querySelectorAll('[data-i18n-title]');
        titles.forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            el.title = this.t(key);
        });
        
        // Reinitialize Lucide icons in case the HTML replacement broke references
        if (window.lucide) {
            window.lucide.createIcons();
        }
    }
}

export const i18n = new I18nManager();
window.i18n = i18n; // Expose globally for simplicity
